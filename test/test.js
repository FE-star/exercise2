var add = require('../lib/add')

describe('大数相加add方法', function () {
  it('字符串"42329"加上字符串"21532"等于"63861"', function () {
    add('42329', '21532')
      .should.equal('63861')
  })
  it('两个数字位数相同："843529812342341234"加上"236124361425345435"等于"1079654173767686669"', function () {
    add('843529812342341234', '236124361425345435')
      .should.equal('1079654173767686669')
  })
  it('两个数字位数不相同："8435298123423412340"加上"236124361425345435"等于"8671422484848757775"', function () {
    add('8435298123423412340', '236124361425345435')
      .should.equal('8671422484848757775')
  })
})