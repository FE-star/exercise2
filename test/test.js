var add = require('../lib/add')

describe('大数相加add方法', function () {
  it('字符串"42329"加上字符串"21532"等于"63861"', function () {
    add('42329', '21532')
      .should.equal('63861')
  })
  it('"843529812342341234"加上"236124361425345435"等于"1079654173767686669"', function () {
    add('843529812342341234', '236124361425345435')
      .should.equal('1079654173767686669')
  })
  it('"1111111111111111111111111111111"加上"8956231798972456413789713154782"等于"10067342910083567524900824265893"', function () {
    add('1111111111111111111111111111111', '8956231798972456413789713154782')
      .should.equal('10067342910083567524900824265893')
  })
  it('"1"加上"1"等于"2"', function() {
    add('1', '1')
      .should.equal('2')
  })
})




